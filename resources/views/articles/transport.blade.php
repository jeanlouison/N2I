  <p>Vous ne savez pas comment vous déplacer à moindre coût ? Voici quelques alternatives.<p>
  <section>
    <h1>Le vélo</h1>
    <p>Le saviez vous ? Il existe en France 15 780 kilomètres de piste cyclable. <br>
      Pourquoi ne pas en profiter pour vous déplacer à moindres frais. <br>
      Vous n'êtes pas pas bricoleur ou n'avez pas le matériel nécessaire à l'entretien d'un vélo ?<br>
      Rendez vous à l'un des ateliers membre du <a href="https://www.heureux-cyclage.org/les-ateliers-en-france">réseau Heureux cyclage</a>.<br></p>
    </section>
